     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        if(!is_null($user)){
            $user->delete();
        }
        return;
    }

    public function restore($id)
    {
        $user = User::withTrashed()->find($id);
        if(!is_null($user)){
            $user->restore();
        }
        return;
    }

    public function trash(){
        $users = User::onlyTrashed()->get();
        $data = compact('users');
